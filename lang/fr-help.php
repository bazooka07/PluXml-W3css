<h1>W3css: Th&egrave;me pour PluXml</h1>

<p>
Le th&egrave;me W3css reprend le framework du m&ecirc;me nom.
La structure et l'organisation du th&egrave;me (articles, pages, cat&eacute;gories, tags, archives, ...) est proche du th&egrave;me d'origine de PluXml.
L'objectif est de fournir un th&egrave;me &eacute;l&eacute;gant tout en profitant des fonctions et de la simplicit&eacute; de W3css pour aboutir &agrave; un site/blog homog&egrave;ne.
</p>


<p>Le th&egrave;me est disponible sous la licence MIT.</p>


<h2>Fonctions principales</h2>

<ul>
	<li>Th&egrave;me responsive design pour s'adpater &agrave; tous les &eacute;crans</li>
	<li>Int&eacute;gration de la police d'ic&ocirc;nes FontAwesome</li>
	<li>Un menu d&eacute;roulant pour les pages statiques</li>
	<li>Diff&eacute;rents th&egrave;mes de couleur disponibles (avec le plugin  plxW3cssThemeSelect)</li>
	<li>La possibilit&eacute; de cr&eacute;er un th&egrave;me de couleur facilement</li>
	<li>Une pagination simplifi&eacute;e (avec le plugin plxSimplePager)</li>
</ul>

<h2>Pr&eacute;-requis</h2>
<ul>
	<li>PluXml 5.5</li>
	<li>Plugin plxFontAwesome</li>
	<li>Plugin plxSimplePager</li>
</ul>

<h2>Documentation</h2>

<h3>Installation du th&egrave;me</h3>

<p>Pour installer le th&egrave;me w3css, il faut pr&eacute;alablement t&eacute;l&eacute;charger l'archive .</p>
<p>Une fois l'archive décompressée, envoyer le dossier "w3css" (renommer le si besoin) via FTP sur votre site et le déposer dans "/themes".</p>
<p>Après avoir copié le th&egrave;me dans votre PluXml, il ne vous reste plus qu’à l’activer. Pour ça, il faut se connecter sur le site en tant qu'administrateur et aller dans le menu: Paramètres > Th&egrave;mes.</p>


<h3>Modifier la couleur du th&egrave;me</h3>

<p>Il existe deux solutions pour modifier la couleur de votre th&egrave;me: modifier directement la feuille de style ou utiliser le plugin plxW3cssThemeSelect.</p>

<p>plxW3cssThemeSelect est un plugin qui permet de choisir le th&egrave;me &agrave; utiliser directement depuis l'administration du site.</p>
<p>Pour installer le plugin, il faut pr&eacute;alablement par t&eacute;l&eacute;charger l'archive.
<br />
Une fois l'archive d&eacute;compress&eacute;, envoyer le dossier "plxW3cssThemeSelect" (renommer le si besoin) via FTP sur votre site et le d&eacute;poser dans "/plugins"
<br />
Une fois le th&egrave;me activ&eacute;, il faut aller dans la configuration du plugin et s&eacute;lectionner la couleur.
<br />
Une fois valid&eacute;, il faut recharger la page de votre site pour que les modifications s'appliquent.</p>

<p>Il est possible de cr&eacute;er diff&eacute;rents th&egrave;mes de couleur avec  <a href="http://www.w3schools.com/w3css/w3css_color_themes.asp" target="_blank" >W3.CSS Color Themes</a>. </p>
<p>Une fois le th&egrave;me cr&eacute;er, il suffit de r&eacute;cup&eacute;rer le CSS qui a &eacute;t&eacute; g&eacute;n&eacute;r&eacute;.
<br />
Avec le plugin plxW3cssThemeSelect, copier le CSS dans un fichier que l'on pourra nommer "<strong>couleur.css</strong>".  Il faut ensuite envoyer le fichier "couleur.css" via FTP sur votre site et le déposer dans "/plugins/plxW3cssThemeSelect/themes" .
<br />
Sans plugin, il faut ajouter le CSS g&eacute;n&eacute;r&eacute; dans la feuille de style "/themes/w3css/css/w3-custom.css". Une fois les modifications effectu&eacute;es, il faut recharger la page de votre site pour que les modifications s'appliquent. Cette solution &agrave; l'avantge de ne pas charger de feuille de style suppl&eacute;mentaire.
</p>

