<h2>Documentation</h2>

<h3>Installation du th&egrave;me</h3>

<p>Pour installer le th&egrave;me, t&eacute;l&eacute;charger le directement depuis  <a href="https://blog.niqnutn.com/plugins/repository/index.php">http://blog.niqnutn.com/plugins/repository/index.php</a> ou <a href="https://github.com/nIQnutn/PluXml-W3css">https://github.com/nIQnutn/PluXml-W3css</a>.</p>
<p>Une fois l'archive décompressée, il faut envoyer le dossier "W3css" via FTP sur votre site et le déposer dans "/themes".</p>
<p>Après avoir copié le th&egrave;me dans votre PluXml, il ne vous reste plus qu’à l’activer. Pour ça, il faut se connecter sur le site en tant qu'administrateur et aller dans le menu: Paramètres > Th&egrave;mes.</p>

<h3>Modifier la couleur du th&egrave;me</h3>

<p>Il existe deux solutions pour modifier la couleur de votre th&egrave;me: modifier directement la feuille de style ou utiliser le plugin plxW3cssThemeSelect.</p>

<p>La solution la plus simple est d'installer et activer le plugin plxW3cssThemeSelect.</p>
<p>Une fois le th&egrave;me activ&eacute;, il faut aller dans la configuration du plugin et s&eacute;lection la couleur.</p>
<p>Une fois valid&eacute;, il faut recharger la page de votre site pour que les modifications s'appliquent.</p>

<p>Il est possible de cr&eacute;er diff&eacute;rents th&egrave;mes de couleur sur  <a href="http://www.w3schools.com/w3css/w3css_color_themes.asp">http://www.w3schools.com/w3css/w3css_color_themes.asp</a>. 
Il suffit de r&eacute;cup&eacute;rer le CSS qui a &eacute;t&eacute; g&eacute;n&eacute;r&eacute; et le recopier dans un fichier que l'on peut nommer <strong>couleur.css</strong>. 
il faut ensuite envoyer le fichier "couleur.css" via FTP sur votre site et le déposer dans "/plugins/plxW3cssThemeSelect/themes" .
</p>


<p>La deuxi&egrave;me solution consiste &agrave; d'&eacute;diter directement la feuille </p>
<p>R&eacute;cup&eacute;rer et &eacute;diter la feuille de style /themes/w3css/css/w3-custom.css</p>
<p>Une fois les modifications effectu&eacute;es, il faut recharger la page de votre site pour que les modifications s'appliquent</p>
<p>Cette solution &eacute;vite de charger une feuille de style suppl&eacute;mentaire.</p>

