<h1>W3css: Th&egrave;me pour PluXml</h1>

<p>
Le th&egrave;me W3css reprend le framework du m&ecirc;me nom.
La structure et l'organisation des pages est fortement inspir&eacute; du th&egrave;me d'origine de PluXml.
L'objectif est de fournir un th&egrave;me &eacute;lgant tout en profitant des autres &eacute;l&eacute;ments de pr&eacute;sentation de W3css  pour aboutir &agrave; un site/blog homog&egrave;ne.
</p>

<p>Le th&egrave;me W3css est disponible sur <a href="https://blog.niqnutn.com/plugins/repository/index.php">http://blog.niqnutn.com/plugins/repository/index.php</a> ou <a href="https://github.com/nIQnutn/PluXml-W3css">https://github.com/nIQnutn/PluXml-W3css</a>.</p>

<p>Le th&egrave;me est disponible sous la licence XXXX.</p>


<h2>Fonctions principales</h2>

<ul>
	<li>Un menu d&eacute;roulant</li>
	<li>Th&egrave;me responsive pour s'adpater &agrave; tous les &eacute;crans</li>
	<li>Int&eacute;gration de la police d'ic&ocirc;nes FontAwesome ( <a href="https://blog.niqnutn.com/plugins/repository/index.php">http://blog.niqnutn.com/plugins/repository/index.php</a> ou <a href="https://github.com/nIQnutn/plxFontAwesome">https://github.com/nIQnutn/plxFontAwesome</a>)</li>
